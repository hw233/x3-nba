package com.ftkj.cfg;


/**
 * 球员图鉴消耗配置
 * @author Jay
 * @time:2017年10月29日 下午4:03:32
 */
public class ArchiveBean {

	private int type;
	private int start;
	private int ended;
	private int needNum;
	
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getStart() {
		return start;
	}
	public void setStart(int start) {
		this.start = start;
	}
	public int getEnded() {
		return ended;
	}
	public void setEnded(int ended) {
		this.ended = ended;
	}
	public int getNeedNum() {
		return needNum;
	}
	public void setNeedNum(int needNum) {
		this.needNum = needNum;
	}
	
}
