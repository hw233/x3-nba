package com.ftkj.cfg;

public class TacticsStudyBean {

	// 研究数量，针对类型的数量
	private int studyNum;
	private int needLv;
	private int needGlod;
	
	public int getStudyNum() {
		return studyNum;
	}
	public void setStudyNum(int studyNum) {
		this.studyNum = studyNum;
	}
	public int getNeedLv() {
		return needLv;
	}
	public void setNeedLv(int needLv) {
		this.needLv = needLv;
	}
	public int getNeedGlod() {
		return needGlod;
	}
	public void setNeedGlod(int needGlod) {
		this.needGlod = needGlod;
	}
	
}
